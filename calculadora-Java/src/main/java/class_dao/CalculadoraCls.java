package class_dao;

/**
 *
 * @author vivia
 */
public class CalculadoraCls {

    private float calculo;
    private int use;

    public CalculadoraCls() {
    }

    public CalculadoraCls(float calculo, int use) {
        this.calculo = calculo;
        this.use = use;
    }

    /**
     * @return the calculo
     */
    public float getCalculo() {
        return calculo;
    }

    /**
     * @param calculo the calculo to set
     */
    public void setCalculo(float calculo) {
        this.calculo = calculo;
    }

    /**
     * @return the use
     */
    public int getUse() {
        return use;
    }

    /**
     * @param use the use to set
     */
    public void setUse(int use) {
        this.use = use;
    }
    
    
}
